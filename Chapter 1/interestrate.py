# Define the interest rate in percent
annual_interest_rate = 5.0

# Define the number of years
number_of_years = 3

# Define the initial amount
principal_amount = 1000

# Calculate the final amount due to interest rate
final_amount = principal_amount * (1 + annual_interest_rate / 100) ** number_of_years
print(final_amount)  # print the result
