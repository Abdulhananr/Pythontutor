
# Here's an example of using some basic math operations in Python:

# Addition
a = 5
b = 3
result = a + b
print(result)  # Output: 8

# Subtraction
a = 10
b = 4
result = a - b
print(result)  # Output: 6

# Multiplication
a = 7
b = 2
result = a * b
print(result)  # Output: 14

# Division
a = 12
b = 3
result = a / b
print(result)  # Output: 4.0

# Modulus (remainder)
a = 17
b = 5
result = a % b
print(result)  # Output: 2


import math

# Trigonometric function (sine)
x = math.pi / 2
result = math.sin(x)
print(result)  # Output: 1.0

# Logarithmic function (natural log)
x = 10
result = math.log(x)
print(result)  # Output: 2.302585092994046
