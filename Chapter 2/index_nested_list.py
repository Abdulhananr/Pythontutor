# Define a nested list
q = [['a', 'b', 'c'], ['d', 'e', 'f'], ['g', 'h']]

# Print the element at index 0 of the first sublist
print(q[0][0])  # a

# Print the second sublist
print(q[1])     # ['d', 'e', 'f']

# Print the element at index 1 of the third sublist
print(q[2][1])  # h

# Print the element at index 0 of the second sublist
print(q[1][0])  # d
